def get_all_organs():
    organs = set()
    for system in organ_systems.values():
        organs.update(system['organs'])
    return list(organs)

def get_all_symptoms():
    symptoms = set()
    for system in organ_systems.values():
        symptoms.update(system['symptoms'])
    return list(symptoms)

def get_organs_by_symptom(symptom: str):
    organs = set()
    for system in organ_systems.values():
        if symptom in system['symptoms']:
            organs.update(system['organs'])
    return list(organs)

def get_doctors_by_organ(organ: str):
    doctors = set()
    for doctor, organs in doctor_specializations.items():
        if organ in organs:
            doctors.add(doctor)
    return list(doctors)

def get_doctors_by_symptoms(symptoms: list):
    organs = set()
    for symptom in symptoms:
        organs.update(get_organs_by_symptom(symptom))
    doctors = set()
    for organ in organs:
        doctors.update(get_doctors_by_organ(organ))
    return list(doctors)




